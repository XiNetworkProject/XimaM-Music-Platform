const { MongoClient } = require('mongodb');
const { createClient } = require('@supabase/supabase-js');
const { v4: uuidv4 } = require('uuid');
const config = require('./migrate-config');

// Initialiser Supabase
const supabase = createClient(config.supabase.url, config.supabase.serviceKey);

// Map pour stocker les correspondances MongoDB ID -> Supabase UUID
const idMapping = new Map();

// Fonction pour g√©n√©rer un UUID valide √† partir d'un ID MongoDB
function generateValidUUID(mongoId) {
  if (idMapping.has(mongoId)) {
    return idMapping.get(mongoId);
  }
  
  const uuid = uuidv4();
  idMapping.set(mongoId, uuid);
  return uuid;
}

async function migrateUsersToSupabaseAuth() {
  console.log('üîê Migration des utilisateurs vers Supabase Auth...');
  
  try {
    const mongoClient = await MongoClient.connect(config.mongodb.uri);
    const db = mongoClient.db();
    const users = await db.collection('users').find({}).toArray();
    
    console.log(`üìä ${users.length} utilisateurs trouv√©s dans MongoDB`);
    
    for (const user of users) {
      console.log(`‚ûï Migration utilisateur ${user.name || user.username}...`);
      
      try {
        // G√©n√©rer un UUID pour l'utilisateur
        const supabaseUserId = generateValidUUID(user._id.toString());
        
        // Cr√©er l'utilisateur dans Supabase Auth
        const { data: authData, error: authError } = await supabase.auth.admin.createUser({
          email: user.email || `${user.username || user.name}@ximam.com`,
          password: 'XimaM2024!', // Mot de passe temporaire s√©curis√©
          email_confirm: true,
          user_metadata: {
            name: user.name || user.username || '',
            username: user.username || user.name || '',
            original_mongo_id: user._id.toString()
          }
        });
        
        if (authError) {
          if (authError.message.includes('already registered')) {
            console.log(`‚è≠Ô∏è Utilisateur ${user.name} d√©j√† existant dans Supabase Auth`);
            // R√©cup√©rer l'ID existant
            const { data: existingUser } = await supabase.auth.admin.listUsers();
            const foundUser = existingUser.users.find(u => 
              u.email === (user.email || `${user.username || user.name}@ximam.com`)
            );
            if (foundUser) {
              idMapping.set(user._id.toString(), foundUser.id);
              console.log(`‚úÖ ID r√©cup√©r√© pour ${user.name}: ${foundUser.id}`);
            }
          } else {
            console.error(`‚ùå Erreur cr√©ation auth utilisateur ${user.name}:`, authError);
            continue;
          }
        } else {
          // Utiliser l'ID retourn√© par createUser
          idMapping.set(user._id.toString(), authData.user.id);
          console.log(`‚úÖ Utilisateur ${user.name} cr√©√© dans Supabase Auth avec ID: ${authData.user.id}`);
        }
        
      } catch (error) {
        console.error(`‚ùå Erreur migration utilisateur ${user.name}:`, error);
      }
    }
    
    await mongoClient.close();
    console.log('‚úÖ Migration des utilisateurs vers Supabase Auth termin√©e');
    
  } catch (error) {
    console.error('‚ùå Erreur migration utilisateurs vers Auth:', error);
  }
}

